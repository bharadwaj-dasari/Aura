function ColorlibStepIcon(props) {
  const { active, completed, className } = props;

  const icons = {
    1: <AssignmentIcon />,
    2: <GroupAddIcon />,
    3: <VideoLabelIcon />,
  };

  return (
    <ColorlibStepIconRoot ownerState={{ completed, active }} className={className}>
      {icons[String(props.icon)]}
    </ColorlibStepIconRoot>
  );
}

ColorlibStepIcon.propTypes = {
  /**
   * Whether this step is active.
   * @default false
   */
  active: PropTypes.bool,
  className: PropTypes.string,
  /**
   * Mark the step as completed. Is passed to child components.
   * @default false
   */
  completed: PropTypes.bool,
  /**
   * The label displayed in the step icon.
   */
  icon: PropTypes.node,
};

const steps = ['Registered', 'Verifiying Facility details', 'Admin approval'];

export default function Approval() {
    const navigate= useNavigate();
    const [status,setStatus] = useState(2);
    const {userEmail}=useAuth();
    const email = userEmail || localStorage.getItem('userEmail');
    useEffect(() => {
        const fetchApprovalStatus = async () => {
            try {
                const response = await axios.get(`http://localhost:5000/api/facility/approval/${email}`);
                setStatus(response.data.status);
            } catch (error) {
                console.error('Error fetching approval status:', error);
            }
        };
        fetchApprovalStatus();
    }, [email]);
  return (
    // <div id='approvalBox' style={{width:'100vw', height:'100vh', display:'flex',flexDirection:'column-reverse'}}>

    <Stack sx={{ width: '100%', position:'absolute',top:'40%' }} spacing={4}>
      {/* <Stepper alternativeLabel activeStep={1} connector={<QontoConnector />}>
        {steps.map((label) => (
          <Step key={label}>
            <StepLabel StepIconComponent={QontoStepIcon}>{label}</StepLabel>
          </Step>
        ))}
      </Stepper> */}
      <Stepper alternativeLabel activeStep={status} connector={<ColorlibConnector />}>
        {steps.map((label) => (
          <Step key={label}>
            <StepLabel StepIconComponent={ColorlibStepIcon}>{label}</StepLabel>
          </Step>
        ))}
      </Stepper>
      <motion.button
            style={{width:'200px', height:'50px', margin:'auto', marginTop:'100px', backgroundColor:'#784af4', color:'white', borderRadius:'5px', border:'none'}}
            whileHover={{ scale: 1.02 }}
            whileTap={{ scale: 0.98 }}
            type="submit"
            onClick={() => {
              navigate('/');
            }}
          >
            Back to Home
        </motion.button>
    </Stack>
    // </div>
  );
}
